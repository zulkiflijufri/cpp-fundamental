#include <iostream>

using namespace std;

int main()
{
	int a = 7;
	int b = 4;
	int hasil;

	//operator +, -, *, /, %

	hasil = a + b;
	cout << a << " + " << b << " = " << hasil << endl;

	hasil = a - b;
	cout << a << " - " << b << " = " << hasil << endl;

	hasil = a * b;
	cout << a << " * " << b << " = " << hasil << endl;

	hasil = a / b;
	cout << a << " / " << b << " = " << hasil << endl;

	hasil = a % b;
	cout << a << " % " << b << " = " << hasil << "\n" << endl;

	return 0;
}
